#!/bin/bash

echo "🚀 Запуск локального сервера разработки..."

# Останавливаем все процессы на порту 8000
echo "🛑 Останавливаем существующие процессы на порту 8000..."
lsof -ti:8000 | xargs kill -9 2>/dev/null || true

# Переходим в папку web
cd web

# Запускаем сервер
echo "🌐 Запускаем сервер на http://localhost:8000"
echo "📁 Рабочая директория: $(pwd)"
echo ""
echo "💡 Для остановки сервера нажмите Ctrl+C"
echo "🌍 Сайт доступен по адресу: http://localhost:8000"
echo ""

python -m http.server 8000
